public class casting {
    public static void main(String[] args) {
        // implicit casting

        double price = 100.0;
        double finalprice = price + 18;
        System.out.println(finalprice);

        // explicit casting

        int p = 100;
        int fp = p + (int)18.99;
        System.out.println(fp);
    }
}


